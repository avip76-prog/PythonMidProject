    "try:\n",
    "    df = load_data()\n",
    "    \n",
    "    st.title(\"📊 Amazon Business Insights Dashboard\")\n",
    "\n",
    "    # סרגל צד לבחירת קטגוריה\n",
    "    categories = sorted(df['main_category'].dropna().unique())\n",
    "    selected_cat = st.sidebar.selectbox(\"בחר קטגוריה:\", categories)\n",
    "\n",
    "    # סינון הנתונים\n",
    "    filtered_df = df[df['main_category'] == selected_cat].copy()\n",
    "\n",
    "    if not filtered_df.empty:\n",
    "        \n",
    "        # --- הגדרת גובה אחיד לגרפים העליונים ---\n",
    "        upper_charts_height = 400\n",
    "\n",
    "        # חלוקה לעמודות עבור החלק העליון\n",
    "        col1, col2 = st.columns([1, 1])\n",
    "\n",
    "        with col1:\n",
    "            # א. גרף המד-אוץ\n",
    "            avg_rating = filtered_df['rating'].mean()\n",
    "            fig_gauge = go.Figure(go.Indicator(\n",
    "                mode = \"gauge+number\",\n",
    "                value = avg_rating,\n",
    "                title = {'text': f\"מדד איכות: {selected_cat}\", 'font': {'size': 20}},\n",
    "                gauge = {\n",
    "                    'axis': {'range': [0, 5], 'tickwidth': 1},\n",
    "                    'bar': {'color': \"black\"},\n",
    "                    'steps': [\n",
    "                        {'range': [0, 3.5], 'color': '#FF4B4B'},\n",
    "                        {'range': [3.5, 4.2], 'color': '#FFA500'},\n",
    "                        {'range': [4.2, 5], 'color': '#238636'}\n",
    "                    ],\n",
    "                    'threshold': {\n",
    "                        'line': {'color': \"white\", 'width': 4},\n",
    "                        'thickness': 0.75,\n",
    "                        'value': 4.0\n",
    "                    }\n",
    "                }\n",
    "            ))\n",
    "            fig_gauge.update_layout(height=upper_charts_height, margin=dict(t=80, b=20))\n",
    "            st.plotly_chart(fig_gauge, use_container_width=True)\n",
    "\n",
    "            # ב. טבלת מדדים מתחת למד האוץ\n",
    "            st.markdown(\"### סיכום מדדי קטגוריה\")\n",
    "            metrics_table = pd.DataFrame({\n",
    "                \"מדד\": [\"כמות מכירות (Reviews)\", \"ממוצע דירוג\", \"כמות מוצרים\"],\n",
    "                \"ערך\": [\n",
    "                    f\"{filtered_df['rating_count'].sum():,.0f}\",\n",
    "                    f\"{avg_rating:.2f} ⭐\",\n",
    "                    f\"{len(filtered_df):,}\"\n",
    "                ]\n",
    "            })\n",
    "            st.table(metrics_table)\n",
    "\n",
    "        with col2:\n",
    "            # ג. גרף פאי (Donut) - דומיננטיות מותגים\n",
    "            filtered_df['brand'] = filtered_df['product_name'].str.split().str[0]\n",
    "            brand_stats = filtered_df.groupby('brand')['rating_count'].sum().reset_index()\n",
    "            top_brands = brand_stats.nlargest(7, 'rating_count')\n",
    "            others_sum = brand_stats.sort_values('rating_count', ascending=False)[7:]['rating_count'].sum()\n",
    "            \n",
    "            if others_sum > 0:\n",
    "                top_brands = pd.concat([top_brands, pd.DataFrame({'brand':['Other'], 'rating_count':[others_sum]})])\n",
    "\n",
    "            fig_pie = px.pie(top_brands, values='rating_count', names='brand', \n",
    "                             title=\"דומיננטיות מותגים (נתח שוק)\",\n",
    "                             hole=0.4,\n",
    "                             color_discrete_sequence=px.colors.qualitative.Pastel)\n",
    "            \n",
    "            fig_pie.update_layout(height=upper_charts_height, margin=dict(t=80, b=20))\n",
    "            st.plotly_chart(fig_pie, use_container_width=True)\n",
    "\n",
    "        st.markdown(\"---\")\n",
    "\n",
    "        # --- חלק תחתון: גרף מגמות חודשיות (Bar + Line) ---\n",
