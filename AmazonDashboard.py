    "            go.Bar(x=monthly_data['month_year'], y=monthly_data['rating_count'], \n",
    "                   name=\"סך מכירות (Reviews)\", marker_color='rgba(0, 102, 204, 0.7)'),\n",
    "            secondary_y=False,\n",
    "        )\n",
    "\n",
    "        # גרף Line\n",
    "        fig_combined.add_trace(\n",
    "            go.Scatter(x=monthly_data['month_year'], y=monthly_data['rating'], \n",
